package Week0;

public class Vars {
    public static void main(String[] args) {

        // Create a string variable
        String name = "Daniel";
        // Print that string variable
        System.out.println("Hello " + name + "!");


        String college = "MCTC";
        // Print, or display in the console, the data in a variable
        System.out.println("Your college is " + college);

        int javacredits = 6;
        System.out.println("This class is worth " + javacredits + " credits");

        // Floating point variable: numbers with decimal places
        double todayTemp = 75.5;
        System.out.println("Today's temperature is " + todayTemp + "F");

        for (int i = 1; i <= 5; i++) {
            //TIP Press <shortcut actionId="Debug"/> to start debugging your code. We have set one <icon src="AllIcons.Debugger.Db_set_breakpoint"/> breakpoint
            // for you, but you can always add more by pressing <shortcut actionId="ToggleLineBreakpoint"/>.
            System.out.println("i = " + i);




        }
    }
}
